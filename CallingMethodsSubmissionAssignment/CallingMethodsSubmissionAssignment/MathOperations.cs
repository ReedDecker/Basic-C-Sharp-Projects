// This class contains methods that perform math operations on a given integer.
public class MathOperations
{
    // Method to add 10 to the given number
    public int AddTen(int number)
    {
        // Adds 10 to the input number and returns the result
        return number + 10;
    }

    // Method to multiply the given number by 5
    public int MultiplyByFive(int number)
    {
        // Multiplies the input number by 5 and returns the result
        return number * 5;
    }

    // Method to subtract 3 from the given number
    public int SubtractThree(int number)
    {
        // Subtracts 3 from the input number and returns the result
        return number - 3;
    }
}